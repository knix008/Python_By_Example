A = {'red', 'green', 'blue'}
B = {'yellow', 'red', 'orange'}
print('The Set A : ', A)
print('The Set B : ', B)

# by operator
print('A | B = ', A | B)
# Prints {'blue', 'green', 'yellow', 'orange', 'red'}

# by method
print('A.union(B) : ', A.union(B))
# Prints {'blue', 'green', 'yellow', 'orange', 'red'}