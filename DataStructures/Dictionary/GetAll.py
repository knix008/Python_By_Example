D = {"name": "Bob", "age": 25, "job": "Dev"}
print(D)

# get all keys
print("Keys : ", list(D.keys()))
# Prints ['name', 'age', 'job']

# get all values
print("Values : ", list(D.values()))
# Prints ['Bob', 25, 'Dev']

# get all pairs
print("All : ", list(D.items()))
# Prints [('name', 'Bob'), ('age', 25), ('job', 'Dev')]
